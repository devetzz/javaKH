package chapter01;

public class Calculation {

	public static void main(String[] args) {
		// 입력
		int number1 = 0;
		int number2 = 0;
		number1 = 20;
		number2 = 10;
		

		
		// 연산
		
		// 출력
		System.out.printf("%d + %d = %d\n", number1, number2, number1+number2);
		System.out.printf("%d - %d = %d\n", number1, number2, number1-number2);
		System.out.printf("%d * %d = %d\n", number1, number2, number1*number2);
		System.out.printf("%d / %d = %d\n", number1, number2, number1/number2);
		
		System.out.println("The end");
	}

}
